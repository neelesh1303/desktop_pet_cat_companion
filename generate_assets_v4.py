# Eye setting function
def set_eyes(frame, dx, dy):
    new_frame = list(frame)
    for r_idx in (8, 9, 10):
        row = list(new_frame[r_idx])
        for c in (12, 13, 14, 19, 20, 21):
            row[c] = '3'
        new_frame[r_idx] = "".join(row)
        
    pupil_row = 9 + dy
    row = list(new_frame[pupil_row])
    row[13 + dx] = '1'
    row[20 + dx] = '1'
    new_frame[pupil_row] = "".join(row)
    return new_frame

def generate_image(data, filename, scale=5):
    height = len(data)
    width = len(data[0]) if height > 0 else 0
    img = Image.new('RGBA', (width, height))
    pixels = img.load()
    for y, row in enumerate(data):
        for x, char in enumerate(row):
            pixels[x, y] = COLORS.get(char, (0,0,0,0))
    img = img.resize((width * scale, height * scale), Image.Resampling.NEAREST)
    img.save(filename)
    print(f"Saved {filename}")

